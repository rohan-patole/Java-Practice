package Loops;

public class EvenOrOdd {
    public static void main(String[] args) {


        for (int i = 0; i <=19; i++) {

        if (i % 2 == 0) {
            System.out.println( i + " :This is an even number" );
            }
        else {
            System.out.println( i + " :This is an odd number" );
        }

        }
    }
}
